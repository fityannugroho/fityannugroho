import type { SVGAttributes } from "react";

type OcticonSize = "medium" | "large";

export type OcticonProps = SVGAttributes<SVGSVGElement> & {
  size?: OcticonSize;
};

type BaseOcticonProps = OcticonProps & {
  dPath: { [size in OcticonSize]: string | string[] };
};

export function Octicon({
  dPath,
  className,
  size = "medium",
  ...props
}: BaseOcticonProps) {
  const sizeNum = size === "large" ? 24 : 16;
  const d = dPath[size];
  const defaultPathProps: SVGAttributes<SVGPathElement> = {
    fill: "currentColor",
    clipRule: "evenodd",
    fillRule: "evenodd",
  };

  return (
    <svg
      className={cn("fill-none", className)}
      xmlns="http://www.w3.org/2000/svg"
      viewBox={`0 0 ${sizeNum} ${sizeNum}`}
      width={sizeNum}
      height={sizeNum}
      {...props}
    >
      {Array.isArray(d) ? (
        d.map((path, i) => <path key={i} d={path} {...defaultPathProps} />)
      ) : (
        <path d={d} {...defaultPathProps} />
      )}
    </svg>
  );
}

